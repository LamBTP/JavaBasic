package Mang;

public class DemoMang2 {
    public static void main(String[] args) {
        //Khai báo vaf khởi tạo giá trị cho mảng
        int mangNumber[] = {3, 5, 7, 9, 10}; //tự hiểu size của mảng

        //Duyệt ra dùng
        for (int i = 0; i < mangNumber.length; i++) {
            System.out.println(mangNumber[i]);
        }
        System.out.println("-------");

        //Lấy 1 phần tử cụ thể
        System.out.println(mangNumber[3]);




    }
}
